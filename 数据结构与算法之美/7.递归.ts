function xPowN(x: number, n: number): number {
  if (n === 0) return 1
  if (n < 0) return 1 / xPowN(x, -n)

  if (n % 2 === 0) {
    const half = xPowN(x, n / 2)
    return half * half
  } else {
    const half = xPowN(x, Math.floor(n / 2))
    return half * half * x
  }

}

console.log(xPowN(2, 3));

